namespace lab4agapov;
/// <summary>
/// Клас ScientificPaper, який містить Метод SearchReference
/// для пошуку посилання наукової статті за її унікальним ідентифікатором (ID) у 
/// відсортованому масиві ідентифікаторів. Метод використовує алгоритм бінарного пошуку 
/// для ефективного знаходження позиції цільового ідентифікатора в масиві. Якщо 
/// ідентифікатор знайдено, метод повертає його індекс у масиві; якщо ні, повертає -1, 
/// що вказує на відсутність посилання з таким ID у масиві. 
/// Цей клас може бути використаний для швидкого пошуку наукових статей за їх 
/// унікальними ідентифікаторами в базі даних або колекції статей, що зберігаються у відсортованому вигляді.
/// </summary>
public static class ScientificPaper
{
    /// <summary>
    /// Метод SearchReference, який приймає відсортований масив унікальних
    ///  ідентифікаторів наукових статей та цільовий ідентифікатор для пошуку.
    ///  Метод реалізує алгоритм бінарного пошуку, щоб ефективно знайти позицію 
    /// цільового ідентифікатора в масиві. Якщо ідентифікатор знайдено, метод 
    /// повертає його індекс у масиві; якщо ні, повертає -1, що вказує на 
    /// відсутність посилання з таким ID у масиві. Цей метод дозволяє швидко
    ///  знаходити наукові статті за їх унікальними ідентифікаторами в базі 
    /// даних або колекції статей, що зберігаються у відсортованому вигляді.
    /// </summary>
    /// <param name="identifiers">Відсортований масив унікальних ідентифікаторів наукових статей</param>
    /// <param name="target">Цільовий ідентифікатор для пошуку</param>
    /// <returns>Повертає індекс знайденого ідентифікатора або -1, якщо ідентифікатор не знайдено</returns>
    public static int SearchReference(int[] identifiers, int target)
    {
        int left = 0;
        int right = identifiers.Length - 1;
        while (left <= right)
        {
            // Обчислюємо середину інтервалу без ризику переповнення.
            int mid = left + (right - left) / 2;
            if (identifiers[mid] == target)
            {
                return mid;
            }
            else if (identifiers[mid] < target)
            {
                // Шукана ID більша, тому відкидаємо ліву половину масиву.
                left = mid + 1;
            }
            else
            {
                // Шукана ID менша, тому відкидаємо праву половину масиву.
                right = mid - 1;
            }
        }
        // Якщо цикл завершився, елемент відсутній у відсортованому масиві.
        return -1;
    }
}
